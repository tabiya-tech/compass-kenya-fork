"""
Executor for the Preference Elicitation Agent eval framework.

Wraps the agent so the standard conversation_test_function can drive it.
"""

from pathlib import Path

from app.agent.agent_types import AgentInput, AgentOutput
from app.agent.preference_elicitation_agent.agent import PreferenceElicitationAgent
from app.agent.preference_elicitation_agent.state import PreferenceElicitationAgentState
from app.agent.preference_elicitation_agent.types import PreferenceVector
from app.conversation_memory.conversation_memory_manager import ConversationMemoryManager
from app.conversation_memory.conversation_memory_types import ConversationContext

# Path to offline-optimized vignettes generated by run_offline_optimization.py
_OFFLINE_OUTPUT_DIR = str(
    Path(__file__).parent.parent.parent
    / "app/agent/preference_elicitation_agent/offline_optimization/output"
)


class PreferenceElicitationAgentExecutor:
    """Executes the Preference Elicitation Agent for evaluation."""

    def __init__(
        self,
        conversation_manager: ConversationMemoryManager,
        session_id: int,
        use_adaptive_selection: bool = True,
    ):
        self._agent = PreferenceElicitationAgent(
            use_personalized_vignettes=False,
            use_offline_with_personalization=True,
            offline_output_dir=_OFFLINE_OUTPUT_DIR,
        )
        self._agent.set_state(
            PreferenceElicitationAgentState(
                session_id=session_id,
                use_adaptive_selection=use_adaptive_selection,
            )
        )
        self._conversation_manager = conversation_manager

    def get_preference_vector(self) -> PreferenceVector:
        return self._agent._state.preference_vector

    def get_state(self) -> PreferenceElicitationAgentState:
        return self._agent._state

    async def __call__(self, agent_input: AgentInput) -> AgentOutput:
        context = await self._conversation_manager.get_conversation_context()
        agent_output = await self._agent.execute(agent_input, context)
        await self._conversation_manager.update_history(agent_input, agent_output)
        return agent_output


class PreferenceElicitationAgentGetConversationContextExecutor:
    """Returns the conversation context."""

    def __init__(self, conversation_manager: ConversationMemoryManager):
        self._conversation_manager = conversation_manager

    async def __call__(self) -> ConversationContext:
        return await self._conversation_manager.get_conversation_context()


class PreferenceElicitationAgentIsFinished:
    """
    Checks if the preference elicitation agent has finished.
    Finished when agent signals done OR max vignettes reached.
    """

    def __call__(self, agent_output: AgentOutput) -> bool:
        return agent_output.finished
